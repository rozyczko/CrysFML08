def create_cube_info_type():

    d = {}

    d['nelem']                         = 0
    d['code']                          = 0
    d['edges']                         = 0
    d['ftype']['nelem']                = 'integer'
    d['ftype']['code']                 = 'integer'
    d['ftype']['edges']                = 'integer,dimension(12)'

    return d

