def create_points_interval_type():

    d = {}

    d['np']                            = None
    d['low']                           = None
    d['high']                          = None
    d['ftype']['np']                   = 'integer'
    d['ftype']['low']                  = 'real(kind=cp)'
    d['ftype']['high']                 = 'real(kind=cp)'

    return d

